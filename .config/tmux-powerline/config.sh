# tmux-powerline configuration
# See: https://github.com/erikw/tmux-powerline

# Use Material Monokai theme
export TMUX_POWERLINE_THEME="material-monokai"

# Path to custom themes directory
export TMUX_POWERLINE_DIR_USER_THEMES="$HOME/.config/tmux-powerline/themes"

# Patched font for powerline symbols (uses Nerd Font glyphs)
export TMUX_POWERLINE_PATCHED_FONT_IN_USE="true"

# Status bar settings
export TMUX_POWERLINE_STATUS_JUSTIFICATION="left"
export TMUX_POWERLINE_STATUS_INTERVAL=5

